
public class MyApp {

	// main is a method
	// void -> return type of main
	// static -> property of class i.e. main method is part of class MyApp
	// public -> accessible from anywhere in file system
	// String[] args -> Array of Strings
	//				 => Command Line Arguments
	public static void main(String[] args) {
		
		// System is a built in class in Java
		// out is referring to an Object of PrintWriter Class
		// println is a method of PrintWriter Class to print something on console
		System.out.println("Hello All from Eclipse");
		System.out.println("Be Exceptional");
		System.out.println("Code With Heart and Brain Together :)");
		
		// MODEL
		// CREATE OPERATION
		// Creating a Single Value Container
		int age = 10;
		
		// UPDATE OPERATION
		// We are manipulating the contents in age
		age = 20;
		
		// READ OPERATION
		// Read the contents in age
		System.out.println("age is: "+age);
		
		// When main will finish age container will be deleted automatically :)

		double amount = 10000.45;
		
	}

}

